    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {   
        $doctor = Doctor::where("name" , $request->doc_name)->get() ;
        $doctoremail = $doctor[0]->email ;
        $appointment = new Appointment() ;
        $appointment->doc_name = $request->doc_name ;
        $appointment->name_client = $request->client_name ;
        $appointment->email_client = $request->email_client ;
        $appointment->time = $request->time ;
        $appointment->problem = $request->problem ;
        $appointment->save();
        Mail::to($doctoremail)->send(new Appointments($appointment));
        return response()->json(["message"=>"email sent"]) ;
    }

